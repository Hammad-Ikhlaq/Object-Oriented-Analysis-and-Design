    private void CheckOutButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CheckOutButtonMouseClicked
        dbConnectivity con=new dbConnectivity();

        int is =Integer.valueOf(LoanItemTextField.getText());

            for(int i=0;i<Library.getInstance().Items.size();i++)
            {
                if(Library.getInstance().Items.get(i).id==is)
                {
                    if(Library.getInstance().Items.get(i).issued==true)
                    {
                        
        JOptionPane.showMessageDialog(null,"Error","Checkout Item failed",JOptionPane.PLAIN_MESSAGE);
        return;
                    }
                }
            }
        
        int Bid = Integer.valueOf(LoanNameTextField.getText());
        String i = IssuedDateTextField.getText();
        String d = LoanDateTextField.getText();
        con.IssueItem(is);
        con.insertLoan(Bid,is,d,i);
                Library.getInstance().LoanList = con.getBorrowerLoanList();
        
        Library.getInstance().Items = con.getItems();
    }//GEN-LAST:event_CheckOutButtonMouseClicked

    private void ChecinButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ChecinButtonMouseClicked
        dbConnectivity con=new dbConnectivity();

        int is =Integer.valueOf(CheckinItemTextField.getText());

            for(int i=0;i<Library.getInstance().Items.size();i++)
            {
                if(Library.getInstance().Items.get(i).id==is)
                {
                    if(Library.getInstance().Items.get(i).issued==false)
                    {
                        
        JOptionPane.showMessageDialog(null,"Error","No One took it",JOptionPane.PLAIN_MESSAGE);
        return;
                    
                    }
                    else
                    {
                                int Bid = Integer.valueOf(CheckinNameTextField.getText());
        con.UnIssueItem(is);
        con.Checkin(Bid, is);
                    }
                }
            }
    }//GEN-LAST:event_ChecinButtonMouseClicked

