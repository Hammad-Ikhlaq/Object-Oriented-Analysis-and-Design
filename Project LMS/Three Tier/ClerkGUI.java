    private void ViewButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ViewButtonMouseClicked
         
            int row=0;
            jTextArea1.append("Name:\n ");
            for(int i=0;i<Library.getInstance().borrowers.size();i++)
            {
                if(Library.getInstance().borrowers.get(i).id==id)
                    row=i;
            }
            jTextArea1.append(Library.getInstance().borrowers.get(row).name);
            jTextArea1.append(" password:  ");
            jTextArea1.append(Library.getInstance().borrowers.get(row).password);
            jTextArea1.append(" Address:  ");
            jTextArea1.append(Library.getInstance().borrowers.get(row).address);
            jTextArea1.append(" Phone:  ");
            jTextArea1.append(Library.getInstance().borrowers.get(row).phone);
            jTextArea1.append("\n");
            for(int i=0;i<Library.getInstance().borrowers.get(row).UserLoanList.size();i++)
            {
                jTextArea1.append("Loan Item id: ");
                jTextArea1.append(String.valueOf(Library.getInstance().borrowers.get(row).UserLoanList.get(i).ItemId));
                jTextArea1.append("Due Date: ");
                jTextArea1.append(String.valueOf(Library.getInstance().borrowers.get(row).UserLoanList.get(i).duedate));
                jTextArea1.append("Issue Date: ");
                jTextArea1.append(String.valueOf(Library.getInstance().borrowers.get(row).UserLoanList.get(i).issuedate));
                jTextArea1.append("\n");
            }
            for(int i=0;i<Library.getInstance().borrowers.get(row).UserHoldList.size();i++)
            {
                jTextArea1.append("Hold Item id: ");
                jTextArea1.append(String.valueOf(Library.getInstance().borrowers.get(row).UserHoldList.get(i).HoldItem));
                jTextArea1.append("\n");
            }
    }//GEN-LAST:event_ViewButtonMouseClicked

